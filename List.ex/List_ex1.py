#How do you append an element to the end of a list?
my_list=["milk","bread","butter","jam"]
my_list.append("ghee")
print(my_list)
#How do you insert an element at a specific index in a list?
my_list=["milk","bread","butter","jam"]
my_list.insert(2,"chips")
print(my_list)
#How do you remove an element from a list by value or index?
my_list=["milk","bread","butter","jam"]
my_list.remove("butter")
print(my_list)
#to remove an element by index method
my_list = ["milk", "bread", "butter", "jam"]
my_list.pop(2)  # Removes the element at index 2
print(my_list)
